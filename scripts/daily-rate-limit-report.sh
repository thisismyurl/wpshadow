#!/bin/bash
#
# GitHub Rate Limit Daily Report
# Generates a summary of GitHub API usage
# Usage: ./scripts/daily-rate-limit-report.sh
#

set -e

echo "в•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—"
echo "в•‘     GitHub API Usage Report - $(date '+%Y-%m-%d %H:%M')     в•‘"
echo "в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ"
echo ""

if ! gh auth status &>/dev/null; then
    echo "вќЊ GitHub CLI not authenticated"
    exit 1
fi

RATE_DATA=$(gh api rate_limit 2>/dev/null)

# Function to display resource stats
show_resource() {
    local name="$1"
    local data="$2"
    
    local limit=$(echo "$data" | jq -r '.limit')
    local used=$(echo "$data" | jq -r '.used')
    local remaining=$(echo "$data" | jq -r '.remaining')
    local reset=$(echo "$data" | jq -r '.reset')
    local reset_time=$(date -d "@$reset" "+%H:%M:%S")
    
    local percent=$((100 * remaining / limit))
    
    # Status indicator
    if [ $percent -ge 75 ]; then
        status="вњ…"
    elif [ $percent -ge 50 ]; then
        status="вљЎ"
    elif [ $percent -ge 25 ]; then
        status="вљ пёЏ "
    else
        status="рџљЁ"
    fi
    
    printf "%-25s %s\n" "$name" "$status"
    printf "  %-20s %'d / %'d\n" "Limit:" $limit
    printf "  %-20s %'d (%d%%)\n" "Used:" $used $((100 - percent))
    printf "  %-20s %'d (%d%%)\n" "Remaining:" $remaining $percent
    printf "  %-20s %s\n" "Resets at:" "$reset_time"
    echo ""
}

# Core API
echo "рџ“Љ Core API (REST)"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
CORE_DATA=$(echo "$RATE_DATA" | jq '.resources.core')
show_resource "Status" "$CORE_DATA"

# Search API
echo "рџ”Ќ Search API"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
SEARCH_DATA=$(echo "$RATE_DATA" | jq '.resources.search')
show_resource "Status" "$SEARCH_DATA"

# GraphQL API
echo "рџ“€ GraphQL API"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
GRAPHQL_DATA=$(echo "$RATE_DATA" | jq '.resources.graphql')
show_resource "Status" "$GRAPHQL_DATA"

# Code Search
echo "рџ”¬ Code Search API"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
CODE_SEARCH_DATA=$(echo "$RATE_DATA" | jq '.resources.code_search')
show_resource "Status" "$CODE_SEARCH_DATA"

# Cache statistics
echo "рџ’ѕ Cache Statistics"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
CACHE_DIR="/workspaces/wpshadow/.cache/github"
if [ -d "$CACHE_DIR" ]; then
    CACHE_FILES=$(find "$CACHE_DIR" -name "*.cache" -type f | wc -l)
    CACHE_SIZE=$(du -sh "$CACHE_DIR" 2>/dev/null | cut -f1)
    echo "  Cached responses:    $CACHE_FILES"
    echo "  Cache size:          $CACHE_SIZE"
    
    # Fresh cache count (< 5 minutes old)
    FRESH_CACHE=$(find "$CACHE_DIR" -name "*.cache" -type f -mmin -5 | wc -l)
    echo "  Fresh cache hits:    $FRESH_CACHE"
else
    echo "  No cache directory found"
fi
echo ""

# Recommendations
echo "рџ’Ў Recommendations"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"

CORE_REMAINING=$(echo "$CORE_DATA" | jq -r '.remaining')
CORE_LIMIT=$(echo "$CORE_DATA" | jq -r '.limit')
CORE_PERCENT=$((100 * CORE_REMAINING / CORE_LIMIT))

if [ $CORE_PERCENT -lt 25 ]; then
    echo "  вљ пёЏ  Rate limit low ($CORE_PERCENT% remaining)"
    echo "  вЂў Switch to local operations only"
    echo "  вЂў Use grep_search instead of GitHub search"
    echo "  вЂў Enable cache: export GH_CACHE_VERBOSE=1"
elif [ $CORE_PERCENT -lt 50 ]; then
    echo "  вљЎ Moderate usage ($CORE_PERCENT% remaining)"
    echo "  вЂў Prefer local operations when possible"
    echo "  вЂў Use gh-cached.sh wrapper for repeated queries"
else
    echo "  вњ… Healthy usage ($CORE_PERCENT% remaining)"
    echo "  вЂў Continue normal operations"
    echo "  вЂў Consider using gh-cached.sh for efficiency"
fi

echo ""
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo "Report generated: $(date)"
