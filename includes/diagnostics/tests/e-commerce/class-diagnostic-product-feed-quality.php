					),
					'Availability Mismatch' => array(
						'Error: Says "in stock" but product archived',
						'Fix: Keep inventory synced',
						'Auto-sync: Enable plugin auto-update',
					),
					'Duplicate Products' => array(
						'Error: Same product multiple IDs',
