						'Availability: In stock / Out of stock',
						'Brand: Manufacturer/brand name',
					),
					'Strongly Recommended' => array(
						'Condition: New / Refurbished / Used',
						'Shipping: Shipping weight, cost',
						'Shipping weight: For calculations',
						'Product type: Category hierarchy',
						'GTIN: Barcode (UPC/EAN/ISBN)',
					),
				),
				'setting_up_google_merchant'   => array(
					'Create Merchant Account' => array(
						'URL: merchants.google.com',
						'Account type: Business vs Multi-client',
						'Verify website ownership',
					),
					'Install Feed Plugin' => array(
						'Plugin: Google Product Feed for WooCommerce',
						'Or: WooCommerce Google Product Feed',
						'Settings: Map product attributes',
					),
					'Submit Feed' => array(
						'Merchant Center → Products → Feeds',
						'Create feed: XML or CSV format',
						'Feed URL: Auto-generated by plugin',
						'Submit for review',
					),
				),
				'common_feed_errors'          => array(
					'Missing Images' => array(
						'Error: 30% of products rejected',
