}

/**
 * Diagnostic_Product_Feed_Quality Class
 *
 * Verifies product feed quality.
 *
 * @since 1.2601.2148
 */
class Diagnostic_Product_Feed_Quality extends Diagnostic_Base {

	/**
	 * The diagnostic slug
	 *
	 * @var string
	 */
	protected static $slug = 'product-feed-quality';

	/**
	 * The diagnostic title
	 *
	 * @var string
	 */
	protected static $title = 'Product Feed Quality';

	/**
	 * The diagnostic description
	 *
	 * @var string
	 */
	protected static $description = 'Verifies Google Merchant Center feed';

	/**
	 * The family this diagnostic belongs to
	 *
	 * @var string
	 */
	protected static $family = 'ecommerce';

	/**
	 * Run the diagnostic check.
	 *
	 * @since  1.2601.2148
	 * @return array|null Finding array if feed issues, null otherwise.
	 */
	public static function check() {
		if ( ! class_exists( 'WooCommerce' ) ) {
			return null; // Not e-commerce
		}

		$feed_status = self::check_product_feed();

		if ( $feed_status['is_configured'] ) {
			return null; // Feed appears configured
		}

		return array(
			'id'           => self::$slug,
			'title'        => self::$title,
			'description'  => __( 'Google Merchant Center feed not configured or outdated. Missing products from Shopping ads = no visibility in Google Search/Shopping tab. 64% of traffic comes from Google Shopping.', 'wpshadow' ),
