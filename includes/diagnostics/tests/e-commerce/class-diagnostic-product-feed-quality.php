			'threat_level' => 70,
			'auto_fixable' => false,
			'kb_link'      => 'https://wpshadow.com/kb/product-feed',
			'family'       => self::$family,
			'meta'         => array(
				'feed_detected' => $feed_status['has_feed'] ? 'yes' : 'no',
			),
			'details'      => array(
				'google_shopping_importance'  => array(
					__( '64% of e-commerce traffic from Google Shopping (Littledata)' ),
					__( 'Google Shopping ads convert 15-30% higher than search ads' ),
					__( 'Product feed required for Shopping ads' ),
					__( 'Google Shopping also feeds to Google Images' ),
				),
				'product_feed_requirements'   => array(
					'Required Attributes' => array(
						'ID: Unique product identifier',
