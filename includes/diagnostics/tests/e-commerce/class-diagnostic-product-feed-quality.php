		);
	}

	/**
	 * Check product feed.
	 *
	 * @since  1.2601.2148
	 * @return array Feed status.
	 */
	private static function check_product_feed() {
		// Check for feed plugin
		$has_plugin = is_plugin_active( 'woocommerce-google-product-feed/woocommerce-google-product-feed.php' ) ||
					  is_plugin_active( 'bing-product-ads-feed/bing-product-ads-feed.php' ) ||
					  is_plugin_active( 'woo-merchant-feed/woo-merchant-feed.php' );

		// Check if feed URL accessible
		$feed_url = home_url( '/feed/google_merchant/' );
		$response = wp_remote_head( $feed_url );
		$has_feed = ! is_wp_error( $response ) && wp_remote_retrieve_response_code( $response ) === 200;

		return array(
			'is_configured' => $has_plugin || $has_feed,
			'has_feed'      => $has_feed,
		);
	}
}
