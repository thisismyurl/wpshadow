	 * @var string
	 */
	protected static $title = 'Robots Policy';

	/**
	 * Diagnostic description.
	 *
	 * @var string
	 */
	protected static $description = 'Checks whether a physical robots.txt file is present and that it does not accidentally block all crawlers with a blanket Disallow directive.';

	/**
